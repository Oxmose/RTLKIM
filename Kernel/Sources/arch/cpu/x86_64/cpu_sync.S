global __pause_spinlock

;-------------------------------------------------------------------------------
; CODE
;-------------------------------------------------------------------------------

section .text
;-------------------------------------------------------------------------------
; Pause Spinlock
;
; Param:
;     Input: rdi: Address of the lock

__pause_spinlock:
    push rbp
    mov  rbp, rsp
    push rax
    mov  rax, rdi

__pause_spinlock_entry:
    lock bts dword [rax], 0
    jc   __pause_spinlock_pause

    pop rax
    mov rsp, rbp
    pop rbp
    ret

__pause_spinlock_pause:
    pause
    test  dword [rax], 1
    jnz   __pause_spinlock_pause
    jmp   __pause_spinlock_entry

